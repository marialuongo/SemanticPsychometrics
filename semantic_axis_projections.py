def format_correlation_value(r, p):
    stars = ""

    for threshold, symbol in SIGNIFICANCE_LEVELS:
        if p < threshold:
            stars = symbol
            break

    return f"{r:.2f}".replace("0.", ".") + stars


def format_decimal_value(x):
    if pd.isna(x):
        return ""

    return f"{x:.2f}".replace("0.", ".")


def create_association_table(
    df,
    axes,
    scales,
    scale_labels,
    column_order,
    display_columns
):
    temp = df[
        (df["Axis"].isin(axes)) &
        (df["Scala"].isin(scales))
    ].copy()

    temp["Cell"] = temp.apply(
        lambda row: format_correlation_value(
            row["r_disattenuated_partial"],
            row["p_value_analytical"]
        ),
        axis=1
    )

    temp["Column"] = (
        temp["Scala"].replace(scale_labels)
        + "_"
        + temp["Axis"]
    )

    table = temp.pivot_table(
        index="Formato",
        columns="Column",
        values="Cell",
        aggfunc="first"
    )

    table = table.reindex(ROW_ORDER)
    table = table[column_order]
    table.columns = display_columns

    return table


def create_split_half_table(df):
    temp = df[
        [
            "Formato",
            "Axis",
            "reliability_projection_splithalf"
        ]
    ].copy()

    temp["Cell"] = temp["reliability_projection_splithalf"].apply(
        format_decimal_value
    )

    table = temp.pivot_table(
        index="Formato",
        columns="Axis",
        values="Cell",
        aggfunc="first"
    )

    table = table.reindex(ROW_ORDER)

    column_order = [
        "DEP_CESD",
        "DEP_WORDS",
        "DEP_ZUNG",
        "WOR_STAI",
        "WOR_WORDS",
        "WOR_ZUNG"
    ]

    table = table[column_order]

    return table


def print_table(title_lines, table):
    print("\n" + title_lines[0])

    for line in title_lines[1:]:
        print(line)

    print()
    print(table.to_string())


