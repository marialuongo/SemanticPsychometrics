def make_score_specs():
    axes = [
        "DEP_WORDS", "DEP_CESD", "DEP_ZUNG",
        "WOR_WORDS", "WOR_ZUNG", "WOR_STAI"
    ]

    dep_specs = [
        ("depText", "Write text (Mean)", "Dep_Text_Mean", "agg_mean"),
        ("depText", "Write text (Max Abs)", "Dep_Text_MaxAbs", "agg_max"),
        ("depText", "Write text", "Dep_Text_Std", "std"),
        ("depWord", "Write words", "Dep_Word_Std", "std"),
        ("depPhrase", "Write phrases", "Dep_Phrase_Std", "std"),
        ("depSelect", "Select words", "Dep_Select_Std", "std"),
    ]

    wor_specs = [
        ("worText", "Write text (Mean)", "Wor_Text_Mean", "agg_mean"),
        ("worText", "Write text (Max Abs)", "Wor_Text_MaxAbs", "agg_max"),
        ("worText", "Write text", "Wor_Text_Std", "std"),
        ("worWord", "Write words", "Wor_Word_Std", "std"),
        ("worPhrase", "Write phrases", "Wor_Phrase_Std", "std"),
        ("worSelect", "Select words", "Wor_Select_Std", "std"),
    ]

    specs = []

    for axis in axes:
        base_specs = dep_specs if axis.startswith("DEP") else wor_specs

        for input_col, formato, internal_name, mode in base_specs:
            specs.append({
                "Axis": axis,
                "input_col": input_col,
                "Formato": formato,
                "internal_name": internal_name,
                "mode": mode
            })

    return specs


