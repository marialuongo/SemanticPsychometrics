def bootstrap_wasserstein_ci(x, y, n_boot=N_BOOT_WD):
    rng = np.random.default_rng(RANDOM_STATE)

    x = pd.Series(x).astype(float)
    y = pd.Series(y).astype(float)
    mask = x.notna() & y.notna()
    x = x.loc[mask].values
    y = y.loc[mask].values

    n = len(x)
    if n < 10:
        return np.nan, np.nan, np.nan, np.nan

    vals = []

    for _ in range(n_boot):
        idx = rng.choice(np.arange(n), size=n, replace=True)
        xb = safe_standardize_np(x[idx])
        yb = safe_standardize_np(y[idx])

        if not np.isnan(xb).all() and not np.isnan(yb).all():
            vals.append(wasserstein_distance(xb, yb))

    vals = np.asarray(vals)

    return (
        float(np.mean(vals)),
        float(np.percentile(vals, 2.5)),
        float(np.percentile(vals, 97.5)),
        float(np.std(vals, ddof=1))
    )

def permutation_wasserstein_test(x, y, n_perm=N_PERM_WD):
    rng = np.random.default_rng(RANDOM_STATE)

    x = pd.Series(x).astype(float)
    y = pd.Series(y).astype(float)
    mask = x.notna() & y.notna()
    x = x.loc[mask].values
    y = y.loc[mask].values

    if len(x) < 10 or len(y) < 10:
        return np.nan, np.nan, np.nan

    xz = safe_standardize_np(x)
    yz = safe_standardize_np(y)

    wd_obs = wasserstein_distance(xz, yz)

    pooled = np.concatenate([xz, yz])
    n_x = len(xz)

    null = []

    for _ in range(n_perm):
        p = rng.permutation(pooled)
        xp = safe_standardize_np(p[:n_x])
        yp = safe_standardize_np(p[n_x:])
        null.append(wasserstein_distance(xp, yp))

    null = np.asarray(null)

    p_value = (np.sum(null >= wd_obs) + 1) / (len(null) + 1)
    z_wd = (wd_obs - np.mean(null)) / np.std(null, ddof=1)

    return float(p_value), float(z_wd), float(np.mean(null))

