# CROSS-SECTIONAL SEMANTIC AXIS ANALYSES
# =========================================================
def run_cross_sectional_semantic(
    df_subset,
    subset_name,
    score_specs,
    reliabilities_proiezioni,
    scales
):
    rows = []

    for spec in score_specs:
        axis = spec["Axis"]
        formato = spec["Formato"]
        out_col = f"{axis}__{spec['internal_name']}"

        if out_col not in df_subset.columns:
            continue

        rel_proj = reliabilities_proiezioni.get(out_col, np.nan)

        for scale in scales:
            if scale not in df_subset.columns:
                continue

            mask = df_subset[out_col].notna() & df_subset[scale].notna()
            n = int(mask.sum())

            if n <= 2:
                continue

            # Reverse sign so that higher scores indicate greater symptomatology
            x = -df_subset.loc[mask, out_col].astype(float)
            y = df_subset.loc[mask, scale].astype(float)

            r, p = pearsonr(x, y)
            ci_low, ci_high = correlation_confidence_interval(
                r,
                n,
                confidence=CI_LEVEL
            )

            rel_scale = RELIABILITY_SCALES.get(scale, np.nan)

            rows.append({
                "Subset": subset_name,
                "Analysis_Type": "SEMANTIC_AXIS",
                "Axis": axis,
                "Formato": formato,
                "Scala": scale,
                "N": n,

                "r_pearson": float(r),
                "p_value_analytical": float(p),
                "fisher_z": float(fisher_z_transform(r)),
                "CI_low": ci_low,
                "CI_high": ci_high,

                "reliability_scale": rel_scale,
                "reliability_projection_splithalf": rel_proj,
                "r_disattenuated_partial": disattenuation_partial(r, rel_scale),
                "r_disattenuated_full": correzione_attenuazione(
                    r,
                    rel_proj,
                    rel_scale
                ),

                "Table_value": make_table_value(r, p, ci_low, ci_high)
            })

    return pd.DataFrame(rows)


