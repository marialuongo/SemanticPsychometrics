def plot_axis_with_sentence_projections(
    pos_words,
    neg_words,
    sentences,
    model,
    output_name,
    max_sentence_len=55
):

    pos_emb = model.encode(pos_words, show_progress_bar=False)
    neg_emb = model.encode(neg_words, show_progress_bar=False)
    sent_emb = model.encode(sentences, show_progress_bar=False)


    pos_center = np.mean(pos_emb, axis=0)
    neg_center = np.mean(neg_emb, axis=0)

    axis_vec_hd = pos_center - neg_center
    axis_norm_hd = np.linalg.norm(axis_vec_hd)

    if axis_norm_hd == 0:
        raise ValueError("The positive and negative centroids coincide.")

    axis_unit_hd = axis_vec_hd / axis_norm_hd
    midpoint_hd = (pos_center + neg_center) / 2


    def project_hd(point):
        v = point - midpoint_hd
        scalar = np.dot(v, axis_unit_hd)
        projected_point = midpoint_hd + scalar * axis_unit_hd
        return projected_point, scalar

    sent_proj_hd = []
    sent_scores = []

    for emb in sent_emb:
        proj, score = project_hd(emb)
        sent_proj_hd.append(proj)
        sent_scores.append(score)

    sent_proj_hd = np.array(sent_proj_hd)
    sent_scores = np.array(sent_scores)

    # PCA
    X_all = np.vstack([
        pos_emb,
        neg_emb,
        sent_emb,
        sent_proj_hd,
        pos_center[None, :],
        neg_center[None, :]
    ])

    pca = PCA(n_components=2)
    X_2d = pca.fit_transform(X_all)

    n_pos = len(pos_words)
    n_neg = len(neg_words)
    n_sent = len(sentences)

    pos_2d = X_2d[:n_pos]
    neg_2d = X_2d[n_pos:n_pos + n_neg]
    sent_2d = X_2d[n_pos + n_neg:n_pos + n_neg + n_sent]

    sent_proj_2d = X_2d[
        n_pos + n_neg + n_sent:
        n_pos + n_neg + 2 * n_sent
    ]

    pos_center_2d = X_2d[-2]
    neg_center_2d = X_2d[-1]


    axis_vec_2d = pos_center_2d - neg_center_2d
    axis_norm_2d = np.linalg.norm(axis_vec_2d)

    if axis_norm_2d == 0:
        raise ValueError("The centroids also coincide in the PCA plane.")

    axis_unit_2d = axis_vec_2d / axis_norm_2d
    midpoint_2d = (pos_center_2d + neg_center_2d) / 2

    all_points_2d = np.vstack([
        pos_2d,
        neg_2d,
        sent_2d,
        sent_proj_2d
    ])

    max_dist = np.max(np.linalg.norm(all_points_2d - midpoint_2d, axis=1))
    line_half_length = max_dist * 1.35

    line_start = midpoint_2d - axis_unit_2d * line_half_length
    line_end = midpoint_2d + axis_unit_2d * line_half_length

    # =====================================================
    # PLOT
    # =====================================================
    plt.figure(figsize=(16, 9))

    plt.scatter(
        pos_2d[:, 0],
        pos_2d[:, 1],
        s=70,
        color="blue",
        alpha=0.20
    )

    for i, word in enumerate(pos_words):
        plt.text(
            pos_2d[i, 0],
            pos_2d[i, 1],
            " " + word,
            fontsize=9,
            color="blue",
            alpha=0.45
        )

    plt.scatter(
        neg_2d[:, 0],
        neg_2d[:, 1],
        s=70,
        color="red",
        alpha=0.20
    )

    for i, word in enumerate(neg_words):
        plt.text(
            neg_2d[i, 0],
            neg_2d[i, 1],
            " " + word,
            fontsize=9,
            color="red",
            alpha=0.45
        )


    plt.plot(
        [line_start[0], midpoint_2d[0]],
        [line_start[1], midpoint_2d[1]],
        linestyle="--",
        linewidth=2.8,
        color="red"
    )


    plt.plot(
        [midpoint_2d[0], line_end[0]],
        [midpoint_2d[1], line_end[1]],
        linestyle="--",
        linewidth=2.8,
        color="blue"
    )


    plt.scatter(
        sent_2d[:, 0],
        sent_2d[:, 1],
        s=130,
        color="purple",
        marker="D"
    )

    plt.scatter(
        sent_proj_2d[:, 0],
        sent_proj_2d[:, 1],
        s=120,
        color="black",
        marker="D"
    )

    for i, sentence in enumerate(sentences):

        plt.plot(
            [sent_2d[i, 0], sent_proj_2d[i, 0]],
            [sent_2d[i, 1], sent_proj_2d[i, 1]],
            color="gray",
            linestyle=":",
            linewidth=1.5
        )

        short_sentence = sentence

        if len(short_sentence) > max_sentence_len:
            short_sentence = short_sentence[:max_sentence_len] + "..."

        plt.text(
            sent_2d[i, 0] + 0.01,
            sent_2d[i, 1] + 0.01,
            f"{short_sentence}",
            fontsize=9,
            color="purple"
        )


    plt.grid(False)
    plt.axis("off")

    plt.tight_layout()


    plt.savefig(
        output_name,
        dpi=300,
        format="tiff",
        bbox_inches="tight"
    )

    plt.show()


    df_scores = pd.DataFrame({
        "Sentence_ID": [f"S{i+1}" for i in range(len(sentences))],
        "Sentence": sentences,
        "Projection_score": sent_scores
    }).sort_values("Projection_score")

    print(f"Grafico salvato come: {output_name}")
    return df_scores


