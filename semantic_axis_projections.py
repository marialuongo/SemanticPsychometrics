def fisher_z_transform(r):
    if pd.isna(r):
        return np.nan

    r = np.clip(r, -0.999999, 0.999999)
    return 0.5 * np.log((1 + r) / (1 - r))


def fisher_z_inverse(z):
    return np.tanh(z)


def correlation_confidence_interval(r, n, confidence=0.95):
    if pd.isna(r) or n is None or n <= 3:
        return np.nan, np.nan

    r = np.clip(r, -0.999999, 0.999999)
    z = fisher_z_transform(r)
    se = 1 / np.sqrt(n - 3)
    z_crit = norm.ppf(1 - (1 - confidence) / 2)

    z_low = z - z_crit * se
    z_high = z + z_crit * se

    return float(fisher_z_inverse(z_low)), float(fisher_z_inverse(z_high))


def disattenuation_partial(r_observed, reliability_scale):
    if pd.isna(r_observed) or pd.isna(reliability_scale) or reliability_scale <= 0:
        return np.nan

    corrected = r_observed / np.sqrt(reliability_scale)
    return float(np.clip(corrected, -1.0, 1.0))


def parse_time_groups(df, time_col="Time"):
    time_values = df[time_col].astype(str).str.strip().str.upper()

    mask_t1 = time_values.isin(["1", "T1", "TIME1"])
    mask_t2 = time_values.isin(["2", "T2", "TIME2"])

    return df[mask_t1].copy(), df[mask_t2].copy()


def split_on_internal_capitals(text):
    if not text.strip():
        return []

    parts = re.split(r'(?<!^)(?=\s+[A-Z])', text)
    return [p.strip() for p in parts if p.strip()]


def split_response_units(text, input_col="", mode="std"):
    t_str = str(text) if pd.notna(text) else ""
    t_str = t_str.strip()

    if not t_str or t_str.lower() == "nan":
        return []

    if mode in ["agg_mean", "agg_max"] or "Text" in input_col:
        units = sent_tokenize(t_str)
        return [u.strip() for u in units if u.strip()]

    if "Phrase" in input_col:
        if "\n" in t_str:
            units = t_str.split("\n")
        elif ";" in t_str:
            units = t_str.split(";")
        elif "|" in t_str:
            units = t_str.split("|")
        else:
            units = split_on_internal_capitals(t_str)

            if len(units) <= 1:
                units = sent_tokenize(t_str)

            if len(units) <= 1 and "," in t_str:
                units = [u.strip() for u in t_str.split(",") if u.strip()]

            if len(units) <= 1:
                units = [u.strip() for u in re.split(r"\s+", t_str) if u.strip()]

        return [u.strip() for u in units if u.strip()]

    if "," in t_str:
        units = t_str.split(",")
    else:
        units = re.split(r"\s+", t_str)

    return [u.strip() for u in units if u.strip()]


def clean_text_basic(text):
    t = str(text) if pd.notna(text) else ""
    t = t.strip()
    t = re.sub(r"\s+", " ", t)
    return t


def prepare_text_for_vader(text, input_col="", mode="std"):
    t = clean_text_basic(text)

    if not t or t.lower() == "nan":
        return ""

    if mode in ["agg_mean", "agg_max"] or "Text" in input_col:
        return t

    units = split_response_units(t, input_col=input_col, mode=mode)

    if len(units) == 0:
        return ""

    return ". ".join(units) + "."


def vader_score_single(text, input_col="", mode="std", analyzer=None):
    if analyzer is None:
        analyzer = sia

    prepared = prepare_text_for_vader(
        text,
        input_col=input_col,
        mode=mode
    )

    if not prepared:
        return {
            "sent_neg": np.nan,
            "sent_neu": np.nan,
            "sent_pos": np.nan,
            "sent_compound": np.nan,
            "sent_distress": np.nan
        }

    scores = analyzer.polarity_scores(prepared)

    return {
        "sent_neg": float(scores["neg"]),
        "sent_neu": float(scores["neu"]),
        "sent_pos": float(scores["pos"]),
        "sent_compound": float(scores["compound"]),
        "sent_distress": float(-scores["compound"])
    }


def sentence_level_sentiment(text, analyzer=None):
    if analyzer is None:
        analyzer = sia

    t = clean_text_basic(text)

    if not t or t.lower() == "nan":
        return {
            "sent_mean": np.nan,
            "sent_maxabs": np.nan
        }

    sents = [s.strip() for s in sent_tokenize(t) if s.strip()]

    if len(sents) == 0:
        return {
            "sent_mean": np.nan,
            "sent_maxabs": np.nan
        }

    vals = []

    for s in sents:
        compound = analyzer.polarity_scores(s)["compound"]
        distress = -compound
        vals.append(distress)

    vals = np.array(vals, dtype=float)

    return {
        "sent_mean": float(np.mean(vals)),
        "sent_maxabs": float(vals[np.argmax(np.abs(vals))])
    }


def run_cross_sectional_sentiment_analysis(
    df_subset,
    subset_name,
    sentiment_specs,
    scales
):
    results = []

    for spec in sentiment_specs:
        formato = spec["Formato"]
        domain = spec["Domain"]
        out_col = spec["internal_name"]

        if out_col not in df_subset.columns:
            continue

        for scale_col in scales:
            if scale_col not in df_subset.columns:
                continue

            mask = df_subset[out_col].notna() & df_subset[scale_col].notna()
            n_obs = int(mask.sum())

            if n_obs <= 2:
                continue

            x = df_subset.loc[mask, out_col].astype(float)
            y = df_subset.loc[mask, scale_col].astype(float)

            r_val, p_val = pearsonr(x, y)
            fisher_z = fisher_z_transform(r_val)

            ci_low, ci_high = correlation_confidence_interval(
                r_val,
                n_obs,
                confidence=CI_LEVEL
            )

            rel_scale = RELIABILITY_SCALES.get(scale_col, np.nan)
            r_dis_partial = disattenuation_partial(r_val, rel_scale)

            results.append({
                "Subset": subset_name,
                "Axis": "SENTIMENT",
                "Domain": domain,
                "Formato": formato,
                "Scala": scale_col,
                "N": n_obs,
                "r_pearson": float(r_val),
                "p_value_analytical": float(p_val),
                "fisher_z": float(fisher_z),
                "reliability_scale": rel_scale,
                "r_disattenuated_partial": r_dis_partial,
                "CI_low": ci_low,
                "CI_high": ci_high,
                "Table_value": (
                    f"{r_val:.2f}*** [{ci_low:.2f}, {ci_high:.2f}]"
                    if p_val < 0.001
                    else f"{r_val:.2f}** [{ci_low:.2f}, {ci_high:.2f}]"
                    if p_val < 0.01
                    else f"{r_val:.2f}* [{ci_low:.2f}, {ci_high:.2f}]"
                    if p_val < 0.05
                    else f"{r_val:.2f} [{ci_low:.2f}, {ci_high:.2f}]"
                )
            })

    return pd.DataFrame(results)


