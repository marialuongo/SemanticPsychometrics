def fisher_z_transform(r):
    if pd.isna(r):
        return np.nan
    r = np.clip(r, -0.999999, 0.999999)
    return 0.5 * np.log((1 + r) / (1 - r))


def fisher_z_inverse(z):
    return np.tanh(z)


def correlation_confidence_interval(r, n, confidence=0.95):
    if pd.isna(r) or n is None or n <= 3:
        return np.nan, np.nan

    z = fisher_z_transform(r)
    se = 1 / np.sqrt(n - 3)
    z_crit = norm.ppf(1 - (1 - confidence) / 2)

    return (
        float(fisher_z_inverse(z - z_crit * se)),
        float(fisher_z_inverse(z + z_crit * se))
    )


def disattenuation_partial(r_observed, reliability_scale):
    if pd.isna(r_observed) or pd.isna(reliability_scale) or reliability_scale <= 0:
        return np.nan

    return float(np.clip(r_observed / np.sqrt(reliability_scale), -1.0, 1.0))


def correzione_attenuazione(r_observed, reliability_projection, reliability_scale):
    if (
        pd.isna(r_observed)
        or pd.isna(reliability_projection)
        or pd.isna(reliability_scale)
        or reliability_projection <= 0
        or reliability_scale <= 0
    ):
        return np.nan

    corrected = r_observed / np.sqrt(reliability_projection * reliability_scale)
    return float(np.clip(corrected, -1.0, 1.0))


def parse_time_groups(df, time_col="Time"):
    time_values = (
        df[time_col]
        .astype(str)
        .str.strip()
        .str.upper()
        .str.replace(".0", "", regex=False)
    )

    mask_t1 = time_values.isin(["1", "T1", "TIME1", "BASELINE", "PRE"])
    mask_t2 = time_values.isin(["2", "T2", "TIME2", "FOLLOWUP", "POST"])

    return df[mask_t1].copy(), df[mask_t2].copy()


def make_table_value(r, p, ci_low, ci_high):
    if p < 0.001:
        stars = "***"
    elif p < 0.01:
        stars = "**"
    elif p < 0.05:
        stars = "*"
    else:
        stars = ""

    return f"{r:.2f}{stars} [{ci_low:.2f}, {ci_high:.2f}]"


