def wrap_axis_label(label):
    return label.replace("_", "\n")


def make_format_specific_matrix(df_data, df_methods, formato):

    df_fmt = df_methods[df_methods["Formato"] == formato].copy()

    if df_fmt.empty:
        return pd.DataFrame()

    df_fmt = df_fmt.sort_values("Axis_Order").reset_index(drop=True)

    labels = df_fmt["Axis"].tolist()
    cols = df_fmt["Column"].tolist()

    mat = pd.DataFrame(
        np.nan,
        index=labels,
        columns=labels
    )

    for i in range(len(df_fmt)):
        for j in range(len(df_fmt)):

            c1 = cols[i]
            c2 = cols[j]

            x = pd.to_numeric(df_data[c1], errors="coerce")
            y = pd.to_numeric(df_data[c2], errors="coerce")

            mask = x.notna() & y.notna()

            if mask.sum() < 3:
                continue

            mat.iloc[i, j] = np.corrcoef(x[mask], y[mask])[0, 1]

    return mat


def make_all_format_matrices(df_data, df_methods):

    format_matrices = {}

    for formato in FORMAT_ORDER_FIGURE:

        mat = make_format_specific_matrix(
            df_data=df_data,
            df_methods=df_methods,
            formato=formato
        )

        if not mat.empty:
            format_matrices[formato] = mat

    return format_matrices


def plot_six_matrices_in_one_figure(
    format_matrices,
    output_tiff,
    figure_title=None
):

    # =========================================================
    # SINGLE PLOT: 6 MATRICES IN ONE FIGURE
    # =========================================================

    fig, axes = plt.subplots(
        nrows=2,
        ncols=3,
        figsize=(24, 14)
    )

    axes = axes.flatten()

    vmin = 0
    vmax = 1

    last_im = None

    for ax, formato in zip(axes, FORMAT_ORDER_FIGURE):

        mat = format_matrices.get(formato)

        if mat is None or mat.empty:
            ax.axis("off")
            continue

        data = mat.values.astype(float)

        labels = mat.index.tolist()
        wrapped_labels = [wrap_axis_label(l) for l in labels]

        im = ax.imshow(
            data,
            aspect="auto",
            vmin=vmin,
            vmax=vmax,
            cmap="Blues"
        )

        last_im = im

        ax.set_xticks(np.arange(len(labels)))
        ax.set_yticks(np.arange(len(labels)))

        ax.set_xticklabels(
            wrapped_labels,
            rotation=0,
            fontsize=9,
            ha="center"
        )

        ax.set_yticklabels(
            wrapped_labels,
            fontsize=9
        )

        # =====================================================
        # DISPLAY LARGER VALUES INSIDE THE CELLS
        # =====================================================

        for i in range(data.shape[0]):
            for j in range(data.shape[1]):

                val = data[i, j]

                if not np.isnan(val):

                    ax.text(
                        j,
                        i,
                        f"{val:.2f}".replace("0.", "."),
                        ha="center",
                        va="center",
                        fontsize=15
                    )

        # =====================================================
        # DEP/WOR SEPARATOR
        # =====================================================

        traits = [
            "DEP" if l.startswith("DEP")
            else "WOR" if l.startswith("WOR")
            else "OTHER"
            for l in labels
        ]

        for i in range(1, len(traits)):

            if traits[i] != traits[i - 1]:

                ax.axhline(
                    i - 0.5,
                    color="white",
                    linewidth=2.5
                )

                ax.axvline(
                    i - 0.5,
                    color="white",
                    linewidth=2.5
                )

        ax.set_title(
            formato,
            fontsize=15,
            fontweight="bold",
            pad=12
        )

    # =========================================================
    # COLORBAR
    # =========================================================

    plt.subplots_adjust(
        right=0.88,
        wspace=0.30,
        hspace=0.30
    )

    if last_im is not None:

        cbar_ax = fig.add_axes([0.91, 0.15, 0.02, 0.7])

        cbar = fig.colorbar(
            last_im,
            cax=cbar_ax
        )

        cbar.set_label(
            "Pearson correlation",
            fontsize=13
        )

        cbar.ax.tick_params(labelsize=11)

    # =========================================================
    # SAVE TIFF FILE
    # =========================================================

    plt.savefig(
        output_tiff,
        dpi=300,
        format="tiff",
        bbox_inches="tight"
    )

    plt.show()

    print(f"Combined figure saved as: {output_tiff}")


