def safe_standardize_np(a):
    a = np.asarray(a, dtype=float)
    sd = np.std(a, ddof=1)
    if len(a) < 2 or pd.isna(sd) or sd == 0:
        return np.full(len(a), np.nan)
    return (a - np.mean(a)) / sd

