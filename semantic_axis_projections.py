def split_on_internal_capitals(text):
    if not str(text).strip():
        return []

    return [p.strip() for p in re.split(r'(?<!^)(?=\s+[A-Z])', str(text)) if p.strip()]


def split_response_units(text, input_col="", mode="std"):
    text = str(text) if pd.notna(text) else ""
    text = text.strip()

    if not text or text.lower() == "nan":
        return []

    if mode in ["agg_mean", "agg_max"]:
        return [s.strip() for s in sent_tokenize(text) if s.strip()]

    if "Phrase" in input_col or "phrase" in input_col:
        if "\n" in text:
            units = text.split("\n")
        elif ";" in text:
            units = text.split(";")
        elif "|" in text:
            units = text.split("|")
        else:
            units = split_on_internal_capitals(text)

            if len(units) <= 1:
                units = sent_tokenize(text)

            if len(units) <= 1 and "," in text:
                units = text.split(",")

            if len(units) <= 1:
                units = re.split(r"\s+", text)

        return [u.strip() for u in units if u.strip()]

    if "," in text:
        units = text.split(",")
    else:
        units = re.split(r"\s+", text)

    return [u.strip() for u in units if u.strip()]


def calc_split_half(texts, axis_vector, model, mode, input_col=""):
    half1, half2 = [], []

    for text in texts:
        units = split_response_units(text, input_col=input_col, mode=mode)

        if len(units) == 0:
            half1.append("")
            half2.append("")
        else:
            half1.append(" ".join(units[0::2]))
            half2.append(" ".join(units[1::2]))

    norm_axis = np.linalg.norm(axis_vector)

    if norm_axis == 0:
        return np.nan

    if mode in ["agg_mean", "agg_max"] or "Text" in input_col or "text" in input_col:
        agg_type = "mean" if mode == "agg_mean" else "max"

        s1 = project_and_aggregate(
            half1,
            axis_vector,
            model,
            aggregation=agg_type
        )

        s2 = project_and_aggregate(
            half2,
            axis_vector,
            model,
            aggregation=agg_type
        )

    else:
        emb1, vm1 = encode_texts_batch(half1, model)
        emb2, vm2 = encode_texts_batch(half2, model)

        s1 = np.full(len(texts), np.nan)
        s2 = np.full(len(texts), np.nan)

        idx1 = np.where(np.array(vm1))[0]
        idx2 = np.where(np.array(vm2))[0]

        if len(idx1) > 0:
            s1[idx1] = np.dot(emb1[idx1], axis_vector) / norm_axis

        if len(idx2) > 0:
            s2[idx2] = np.dot(emb2[idx2], axis_vector) / norm_axis

    valid = ~np.isnan(s1) & ~np.isnan(s2)

    if valid.sum() > 2:
        r_half, _ = pearsonr(s1[valid], s2[valid])

        if pd.notna(r_half) and r_half > 0:
            return float((2 * r_half) / (1 + r_half))

    return np.nan


