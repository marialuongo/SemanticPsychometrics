def get_embedding_dim(model):
    return model.get_embedding_dimension()


def encode_texts_batch(texts, model):
    texts = [str(t) if pd.notna(t) else "" for t in texts]
    valid_mask = [t.strip() and t.lower() != "nan" for t in texts]
    valid_texts = [t for t, ok in zip(texts, valid_mask) if ok]

    dim = get_embedding_dim(model)
    embeddings_all = np.full((len(texts), dim), np.nan)

    if len(valid_texts) > 0:
        valid_embeddings = model.encode(valid_texts, show_progress_bar=False)

        idx = 0
        for i, ok in enumerate(valid_mask):
            if ok:
                embeddings_all[i] = valid_embeddings[idx]
                idx += 1

    return embeddings_all, valid_mask


def calc_axis(pos_items, neg_items, model):
    pos_emb = model.encode(pos_items, show_progress_bar=False)
    neg_emb = model.encode(neg_items, show_progress_bar=False)

    return np.mean(pos_emb, axis=0) - np.mean(neg_emb, axis=0)


def project_and_aggregate(texts, axis_vector, model, aggregation="mean"):
    norm_axis = np.linalg.norm(axis_vector)

    if norm_axis == 0:
        return np.array([np.nan] * len(texts))

    scores = []

    for text in texts:
        text = str(text) if pd.notna(text) else ""
        text = text.strip()

        if not text or text.lower() == "nan":
            scores.append(np.nan)
            continue

        sentences = [s.strip() for s in sent_tokenize(text) if s.strip()]

        if len(sentences) == 0:
            scores.append(np.nan)
            continue

        embeddings = model.encode(sentences, show_progress_bar=False)
        projections = np.dot(embeddings, axis_vector) / norm_axis

        if aggregation == "max":
            score = projections[np.argmax(np.abs(projections))]
        else:
            score = np.mean(projections)

        scores.append(score)

    return np.array(scores)


